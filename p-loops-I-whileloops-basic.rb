# While loops will allow us to loop through a particular block of code a specific number of times
# As long as conditions = true, this will keep looping over and executing that same code inside the while loop
# If we want to do something continuously until we need to stop

index = 1
while index <= 5 
    # All the code we put inside here, will continually get executed
    # if condition above remains true
    puts index
    # Increment the index
    # index = index + 1
    index += 1
end